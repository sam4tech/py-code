# Addition
print(20 + 5)

#subtraction
print(60-4)

#multiplication
print(4*8)

#division
print(5/2%1)  # 0.5
      
#comparison operators
x = 10
x += 5
print(x) # 15

y = 5
y //= 5
print(y) #1

#logical

a = 5
b = 10
print( a >= 5 and b <= 10)  #true
print( a >= 5 or b < 10)     #true

# bitwise and & or

print("bitwise")
print(14&13)  #12
print(14|13)      #15

# bitwise left shift 

print(14<<2)      #56

# bitwise right shift

print(23>>1)   # 11

# Membership

print("J" in "Jaipur")  # true

print("j" in "Jaipur")  # false

print("Pur" not in "Jaipur") # true


#Identity operator

s = 10
